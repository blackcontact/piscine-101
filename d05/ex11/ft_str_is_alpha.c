int		ft_str_is_alpha(char *str)
{
	int		result;
	int		i;

	result = 1;
	i = 0;
	while (str[i] != '\0' && result)
	{
		if (!((str[i] >= 'A' && str[i] <= 'Z')
				|| (str[i] >= 'a' && str[i] <= 'z')))
		{
			result = 0;
		}
		i++;
	}
	return (result);
}
